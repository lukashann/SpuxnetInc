class Color(object):

    def __init__(self, red, green, blue, clear):
        self.red = red
        self.green = green
        self.blue = blue
        self.clear = clear

    def get_red(self):
        return self.red

    def get_green(self):
        return self.green

    def get_blue(self):
        return self.blue

    def get_clear(self):
        return self.clear
